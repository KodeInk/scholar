package com.codemovers.scholar.engine.db.entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author mover
 */
@Entity
@Table(name = "school_data")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "SchoolData.findAll", query = "SELECT s FROM SchoolData s")
    , @NamedQuery(name = "SchoolData.findById", query = "SELECT s FROM SchoolData s WHERE s.id = :id")
    , @NamedQuery(name = "SchoolData.findByName", query = "SELECT s FROM SchoolData s WHERE s.name = :name")
    , @NamedQuery(name = "SchoolData.findByExternalId", query = "SELECT s FROM SchoolData s WHERE s.externalId = :externalId")})
public class SchoolData implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Long id;
    @Size(max = 255)
    @Column(name = "name")
    private String name;
    @Size(max = 255)
    @Column(name = "external_id")
    private String externalId;

    public SchoolData() {
    }

    public SchoolData(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getExternalId() {
        return externalId;
    }

    public void setExternalId(String externalId) {
        this.externalId = externalId;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof SchoolData)) {
            return false;
        }
        SchoolData other = (SchoolData) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "SchoolData[ id=" + id + " ]";
    }

}
